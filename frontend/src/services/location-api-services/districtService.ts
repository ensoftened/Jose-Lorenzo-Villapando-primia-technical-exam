import Axios from "axios";
import SelectItemProps from "types/SelectItemProps";

//const endpoint = `${process.env.LOCATION_API_URL}Region/`
const endpoint = `${process.env.REACT_APP_LOCATION_SERVER_API_URL}District/`;

export async function getAllDistricts() {
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////console.log  ("ENDPOINT", endpoint)
  try {
    let allDistricts = await Axios.get(`${endpoint}`);

    return allDistricts;
  } catch (error) {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////console.log  ("ERROR", error)
  }
}

export async function getFilteredDistricts(id: string) {
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////console.log  ("ENDPOINT", endpoint)
  try {
    let allDistricts = await Axios.get(`${endpoint}${id}`);
    allDistricts = allDistricts.data.map((item: any) => {
      return {
        _id: item._id,
        label: item.name,
      };
    });

    return allDistricts;
  } catch (error) {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////console.log  ("ERROR", error)
  }
}
