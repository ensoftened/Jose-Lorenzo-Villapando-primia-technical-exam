export async function getFilteredBarangays(id: string) {
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////console.log  ("ENDPOINT", endpoint)
  try {
    let allBarangays = await Axios.get(`${endpoint}${id}`);
    allBarangays = allBarangays.data.map((item: any) => {
      return {
        _id: item._id,
        label: item.name,
      };
    });

    // //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////console.log  ("allBarangays", allBarangays)
    return allBarangays;
  } catch (error) {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////console.log  ("ERROR", error)
  }
}
