import Axios from "axios";

//const endpoint = `${process.env.LOCATION_API_URL}Region/`
const endpoint = `${process.env.REACT_APP_LOCATION_SERVER_API_URL}Region/`;

export async function getAllRegions() {
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////console.log  ("ENDPOINT", endpoint)
  try {
    let allRegions = await Axios.get(`${endpoint}`);
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////console.log  ("USER REGIONS", allRegions)
    allRegions = allRegions.data.map((item: any) => {
            return {
                _id: item._id,
                label: item.name
            }
        })
    return allRegions;
  } catch (error) {
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////console.log  ("ERROR", error)
  }
}
